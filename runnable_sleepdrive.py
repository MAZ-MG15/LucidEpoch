# SMOTE default k_neighbors is 5. We must ensure k_neighbors < min_samples_in_class.
min_samples = y_combined.value_counts().min()
# Use 2 if possible, otherwise min_samples - 1, respecting user's suggestion for k_neighbors=2
k_adj = min(2, min_samples - 1) if min_samples > 1 else 1

# For multi-class, sampling_strategy must be a dict. Oversample to 60% of majority class.
class_counts = y_combined.value_counts()
majority_class_count = class_counts.max()

sampling_strategy_dict = {}
for cls, count in class_counts.items():
    # Only oversample minority classes, target 60% of the majority class size
    if count < majority_class_count:
        sampling_strategy_dict[cls] = int(0.6 * majority_class_count)
    else:
        sampling_strategy_dict[cls] = count # Keep majority class as is or handle if undersampling is also desired

print(f"Using k_neighbors={k_adj} for SMOTE and a dynamic sampling_strategy dictionary based on 0.6 ratio.")

# Apply SMOTETomek with adjusted neighbors and sampling strategy
